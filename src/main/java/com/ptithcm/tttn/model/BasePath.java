package com.ptithcm.tttn.model;

public class BasePath {
	private String pathName;
	
	public BasePath() {
		super();
	}

	public BasePath(String pathName) {
		super();
		this.pathName = pathName;
	}

	public String getPathName() {
		return pathName;
	}

	public void setPathName(String pathName) {
		this.pathName = pathName;
	}
	
}
